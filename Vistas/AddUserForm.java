    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String nombre = txtName.getText();
        String apellidos = txtLastname.getText();
        // Lista desplegable contiene 5 elementos
        // cbDocumentType[0] = Cédula de ciudadanía
        int posicionLista = cbDocumentType.getSelectedIndex();
        String tipoDocumento = (String)cbDocumentType.getSelectedItem();
        String documento = txtDocument.getText();
        String correo = txtEmail.getText();
        System.out.println("Nombre: " + nombre + " " + apellidos + ", Documento: " + posicionLista + " " + documento + " " + "; correo");
        
                
        if(nombre.isEmpty() || apellidos.isEmpty() || documento.isEmpty() ||correo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Hay Campos sin diligenciar", "Formulario registro", JOptionPane.WARNING_MESSAGE);
        }else {
            String query = "INSERT INTO `empleados`(`nombreEmp`,`apellidos`,`tipoDocumento`,`documento`,`correo`)" + 
                "VALUES ('" + nombre +"','" + apellidos + "','" + tipoDocumento +"','" + documento + "','" + correo + "')";               
                
            try {
                connection = conexion.getConnection();
                st = connection.createStatement();
                //Cuando se crea un empleado se edita un stament
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Registro exitoso.");
            } catch (SQLException e){
                 JOptionPane.showMessageDialog(this, "No se puedo crear el usuario", "Formulario registro", JOptionPane.ERROR_MESSAGE);
            }
            
            this.dispose();
            System.out.println("Nombre: " + nombre + " " + apellidos + ", Documento: " + posicionLista + " " + documento + " " + "; correo");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

